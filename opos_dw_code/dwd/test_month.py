def test_month_sql_task(ds):
    HQL = '''


--插入数据
set hive.exec.parallel=true;
set hive.exec.dynamic.partition.mode=nonstrict;

insert overwrite table opos_dw.test_month partition(country_code,dt)
select
create_year
,create_week
,combine_year_week
,city_id
,sender_id
,first_order
from
opos_dw.dwd_active_user_week_di
where
country_code='nal' 
and dt='{pt}'
limit 1;

'''.format(
        pt=ds,
        after_6_day=airflow.macros.ds_add(ds, +6),
        table=table_name,
        now_day='{{macros.ds_add(ds, +1)}}',
        db=db_name
    )
    return HQL


